export function getReviewsByToolId(toolId: string): Review[] {
  return reviews.filter((review) => review.toolId === toolId);
}

export function getAverageRating(toolId: string): number {
  const toolReviews = getReviewsByToolId(toolId);
  if (toolReviews.length === 0) return 0;
  const sum = toolReviews.reduce((acc, review) => acc + review.rating, 0);
  return sum / toolReviews.length;
}
